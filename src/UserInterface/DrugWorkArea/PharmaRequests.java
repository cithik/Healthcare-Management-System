    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        //MedicineInventoryWorkRequest miwr= (MedicineInventoryWorkRequest)account.getWorkQueue().getWorkRequestList();
        model.setRowCount(0);
        for (WorkRequest wr: enterprise.getWorkQueue().getWorkRequestList()){
            if(wr instanceof MedicineInventoryWorkRequest){
            if(wr.getCompanyReceiver()==account.getCompany()){
            MedicineInventoryWorkRequest miwr = (MedicineInventoryWorkRequest) wr;
            Object[] row = new Object[4];
            row[0] = miwr;
            row[1] = miwr.getMedicinesInventroy().getMedicine();
            row[2] = miwr.getMedicinesInventroy().getQuantity();
            row[3] = miwr.getStatus();
            model.addRow(row);
            }
        }
        }
    }
    
