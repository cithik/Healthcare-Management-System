    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Select a drug");
            return;
        }
        MedicineInventoryWorkRequest miwr = (MedicineInventoryWorkRequest) jTable1.getValueAt(selectedRow, 0);
        if(miwr.getStatus().equals("Requested")){
        Pharmacy pharmacy = (Pharmacy)miwr.getSender().getCompany();
        boolean medicinePresent=false;
        for (MedicineInventroy medicine: pharmacy.getMedicines().getMedicinesList()){
            if(medicine.getMedicine()==miwr.getMedicinesInventroy().getMedicine()){
                medicinePresent=true;
                medicine.setQuantity(medicine.getQuantity()+miwr.getMedicinesInventroy().getQuantity());
            }
        }
        if(!medicinePresent){
            pharmacy.getMedicines().getMedicinesList().add
        (new MedicineInventroy((DrugCompany)account.getCompany(), 
                miwr.getMedicinesInventroy().getMedicine(), miwr.getMedicinesInventroy().getQuantity()));
            JOptionPane.showMessageDialog(null, "Drug sent to pharmacy");
        }
        miwr.setStatus("Processed");
        }else{
            JOptionPane.showMessageDialog(null, "Already Processed");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

