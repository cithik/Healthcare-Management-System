    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Select a specific doctor to view details");
            return;
        }
        Prescription prescription = (Prescription) jTable1.getValueAt(selectedRow, 0);
        PrescriptionJPanel prescriptionJPanel = 
                new PrescriptionJPanel(userProcessContainer, patientObj, prescription, network);
        userProcessContainer.add("ViewPrescription", prescriptionJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_jButton1ActionPerformed

