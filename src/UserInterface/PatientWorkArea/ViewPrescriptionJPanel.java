    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for (Prescription prescription : patientObj.getPrescriptions()){
                Object[] row = new Object[1];
                row[0] = prescription;
                model.addRow(row);
        }
    }
